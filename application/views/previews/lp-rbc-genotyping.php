					<div class="tab" id="about">
						
						<p>
							The LIFECODES RBC and LIFECODES RBC-R products utilize Luminex technology in two multiplex assays for the genotyping of red blood cell (RBC) antigen blood groups. The LIFECODES RBC assay consists of highly prevalent single nucleotide polymorphisms (SNPs) of 6 blood antigen groups. The LIFECODES RBC-R assay consists of rare SNPs of 9 blood antigen groups.</p>	
						<h4>LIFECODES RBC assay</h4>
                        
                        <p>The LIFECODES RBC assay uses single nucleotide polymorphism (SNP) detection by the Polymerase Chain Reaction &#150; Reverse Sequence Specific Oligonucleotide (PCR-RSSO) method. This multiplex assay has been optimized to genotype for the antigens of the following highly prevalent blood groups:</p>
                        <ul>
                          <li>Kell &#150; K, k, Kpa, Kpb, Kpc, Jsa, Jsb </li>
                          <li>Kidd &#150; Jka, Jkb, Jk (Finnish null) </li>
                          <li>Duffy &#150; Fya, Fyb, Fyx (a-b weak), FyGATAsil </li>
                          <li>MNS &#150; M, N, S, s, S-s-Uvar (includes 2 S silencing mutations: &#150; GPB230 and GPB intron +5) </li>
                          <li>Rh &#150; C, c, E, e </li>
                          <li>Dombrock &#150; Doa, Dob </li>
                        </ul>
                        <p>&nbsp;</p>
<h4>LIFECODES RBC-R assay</h4>

<p>The LIFECODES RBC-R assay uses single nucleotide polymorphism (SNP) detection by the Polymerase Chain Reaction &#150; Reverse Sequence Specific Oligonucleotide (PCR-RSSO) method. This multiplex assay has been optimized to genotype for the antigens of the following rare blood groups:</p>
<ul>

                          <li>Colton &#150; Coa, Cob </li>
                          <li>Dombrock &#150; Hy, Joa, Doa, Dob </li>
                          <li>Scianna &#150; Sc1, Sc2 </li>
                          <li>Lutheran &#150; Lua, Lub </li>
                          <li>Diego &#150; Dia, Dib, Wra, Wrb </li>
                          <li>Landsteiner-Weiner &#150; LWa, LWb </li>
                          <li>Cartwright &#150; Yta, Ytb </li>
                          <li>Knops &#150; Kna, Knb, McCa+, McCb+, SI1, SI2 </li>
                          <li>Cromer &#150; Cra</li>
</ul>
